//Para mostrar todos los Proveedores Agregados
 
   private void CargarTablaProveedores(){
       Connection con = Conexion.conectar();
       DefaultTableModel model = new DefaultTableModel();
       String sql = "select * from Proveedor";
       Statement st;
       
       try {
           st = (Statement) con.createStatement();
           ResultSet rs = st.executeQuery(sql);
           InterGestionProveedor.TablaProveedores = new JTable(model);
           InterGestionProveedor.AProveedores.setViewportView(InterGestionProveedor.TablaProveedores);
           
           model.addColumn("idProveedor");
           model.addColumn("empresa");
           model.addColumn("email");
           model.addColumn("telefono");
           
           while(rs.next()){
                Object fila[] = new Object [4];
                
                for(int i =0; i <4; i++){
                   fila[i] = rs.getObject(i + 1); 
                }
                model.addRow(fila);
           
           }
           con.close();
           
       } catch (SQLException e) {
           System.out.println("Error al llenar la tabla Proveedor" + e);
           
       }
      //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        TablaProveedores.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = TablaProveedores.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idProveedor = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosProveedoresSeleccionado(idProveedor);//metodo
                }
            }
        });
    
    
    }
   
   
    /*
     * **************************************************
     * Metodo que envia datos seleccionados
     * **************************************************
     */
    private void EnviarDatosProveedoresSeleccionado(int idProveedor) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(
                    "select * from Proveedor where idProveedor = '" + idProveedor + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                Text_Empresa.setText(rs.getString("empresa"));
                Text_Email.setText(rs.getString("email"));
                Text_Telefono.setText(rs.getString("telefono"));
                
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar Proveedor: " + e);
        }
    }
       
