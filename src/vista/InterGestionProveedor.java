    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        // TODO add your handling code here:
        
        if (Text_Empresa.getText().isEmpty() && Text_Email.getText().isEmpty()&& Text_Telefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa los campos");
        } else {

            Proveedor proveedor = new Proveedor();
            Ctrl_Proveedor controlProveedor = new Ctrl_Proveedor();

            proveedor.setEmpresa(Text_Empresa.getText().trim());
            proveedor.setEmail(Text_Email.getText().trim());
            proveedor.setTelefono(Text_Telefono.getText().trim());
            

            if (controlProveedor.actualizar(proveedor, idProveedor)) {
                JOptionPane.showMessageDialog(null, "Datos del Proveedor actualizado");
                this.CargarTablaProveedores();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar");
            }

        }
        
        
    }//GEN-LAST:event_ActualizarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
         Ctrl_Proveedor controlProveedor = new Ctrl_Proveedor();
        if (idProveedor == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un proveedor");
        } else {
            if (!controlProveedor.eliminar(idProveedor)) {
                JOptionPane.showMessageDialog(null, "Proveedor Eliminado");
                this.CargarTablaProveedores();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar proveedor");
                this.Limpiar();
            }
        }
    }//GEN-LAST:event_EliminarActionPerformed

